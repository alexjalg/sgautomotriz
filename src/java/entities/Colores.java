package entities;

public class Colores 
{
    private String idCol="";
    private String desCol="";
    private String desCol_f="";

    /**
     * @return the idCol
     */
    public String getIdCol() {
        return idCol;
    }

    /**
     * @param idCol the idCol to set
     */
    public void setIdCol(String idCol) {
        this.idCol = idCol;
    }

    /**
     * @return the desCol
     */
    public String getDesCol() {
        return desCol;
    }

    /**
     * @param desCol the desCol to set
     */
    public void setDesCol(String desCol) {
        this.desCol = desCol;
    }

    /**
     * @return the desCol_f
     */
    public String getDesCol_f() {
        return desCol_f;
    }

    /**
     * @param desCol_f the desCol_f to set
     */
    public void setDesCol_f(String desCol_f) {
        this.desCol_f = desCol_f;
    }
}
