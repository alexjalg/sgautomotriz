package entities;

public class TipoCarroceria {
    
    
    private int idTipCar = 0 ;
    private String desTipCar = "";
    private String codAbrTip = "";

    /**
     * @return the idTipCar
     */
    public int getIdTipCar() {
        return idTipCar;
    }

    /**
     * @param idTipCar the idTipCar to set
     */
    public void setIdTipCar(int idTipCar) {
        this.idTipCar = idTipCar;
    }

    /**
     * @return the desTipCar
     */
    public String getDesTipCar() {
        return desTipCar;
    }

    /**
     * @param desTipCar the desTipCar to set
     */
    public void setDesTipCar(String desTipCar) {
        this.desTipCar = desTipCar;
    }

    /**
     * @return the codAbrTip
     */
    public String getCodAbrTip() {
        return codAbrTip;
    }

    /**
     * @param codAbrTip the codAbrTip to set
     */
    public void setCodAbrTip(String codAbrTip) {
        this.codAbrTip = codAbrTip;
    }
    
    
    
}
