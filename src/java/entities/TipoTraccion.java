package entities;

public class TipoTraccion {
    private int idTipTrac;
    private String desTipTrac;

    /**
     * @return the idTipTrac
     */
    public int getIdTipTrac() {
        return idTipTrac;
    }

    /**
     * @param idTipTrac the idTipTrac to set
     */
    public void setIdTipTrac(int idTipTrac) {
        this.idTipTrac = idTipTrac;
    }

    /**
     * @return the desTipTrac
     */
    public String getDesTipTrac() {
        return desTipTrac;
    }

    /**
     * @param desTipTrac the desTipTrac to set
     */
    public void setDesTipTrac(String desTipTrac) {
        this.desTipTrac = desTipTrac;
    }
}
