public class AmbienteUbicacion {

    private int idAmbUbi;
    private String desAmbUbi;    
    private String codTipAmbUbi;
    private String desCorAmbUbi;

    /**
     * @return the idAmbUbi
     */
    public int getIdAmbUbi() {
        return idAmbUbi;
    }

    /**
     * @param idAmbUbi the idAmbUbi to set
     */
    public void setIdAmbUbi(int idAmbUbi) {
        this.idAmbUbi = idAmbUbi;
    }

    /**
     * @return the desCorAmbUbi
     */
    public String getDesCorAmbUbi() {
        return desCorAmbUbi;
    }

    /**
     * @param desCorAmbUbi the desCorAmbUbi to set
     */
    public void setDesCorAmbUbi(String desCorAmbUbi) {
        this.desCorAmbUbi = desCorAmbUbi;
    }

    /**
     * @return the codTipAmbUbi
     */
    public String getCodTipAmbUbi() {
        return codTipAmbUbi;
    }

    /**
     * @param codTipAmbUbi the codTipAmbUbi to set
     */
    public void setCodTipAmbUbi(String codTipAmbUbi) {
        this.codTipAmbUbi = codTipAmbUbi;
    }

    /**
     * @return the desAmbUbi
     */
    public String getDesAmbUbi() {
        return desAmbUbi;
    }

    /**
     * @param desAmbUbi the desAmbUbi to set
     */
    public void setDesAmbUbi(String desAmbUbi) {
        this.desAmbUbi = desAmbUbi;
    }
}
